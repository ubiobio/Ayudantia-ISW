/*
    ! NPM significa "Node Package Manager" y es el administrador de paquetes predeterminado para Node.js.
    ! Es una herramienta que facilita la instalación y gestión de paquetes de código fuente y bibliotecas de Node.js
    ! que pueden ser utilizados en proyectos.

    ? NPM permite a los desarrolladores de Node.js compartir y reutilizar código fácilmente.
    ? NPM también proporciona herramientas para ayudar a los desarrolladores a administrar
    ?  dependencias de paquetes y manejar diferentes versiones de un paquete

    * npm init => Crea un archivo package.json en el directorio actual.
    * npm install => Instala todos los paquetes listados como dependencias en package.json.
    * npm install <package> => Instala un paquete específico y lo agrega a la lista de dependencias en package.json.
    * npm install <package>@<version> => Instala un paquete específico y una versión específica y lo agrega a la lista de dependencias en package.json.
    * npm install <package> --save-dev => ( -D )- Instala un paquete específico y lo agrega a la lista de dependencias de desarrollo en package.json.
    * npm install <package> --global => ( -g ) - Instala un paquete específico de forma global en su sistema.

*/
